/**********************************************************************************************************************
 * Function Name: initDynamicBar
 * Summary:
 *  initialises Bar element with parameters and also maps each to the sub window
 * Parameters:
 * bar      bar element that needs to be initialsed
 * index	index of bar
 * Return:
 *  CYGFX_ERROR     returns status
 **********************************************************************************************************************
 */
static CYGFX_ERROR initDynamicBar(DynamicBar* bar, CYGFX_U32 index)
{
    CYGFX_ERROR ret = CYGFX_OK;
    CYGFX_DISP_WINDOW_PROPERTIES_S winProps = CYGFX_DISP_WINDOW_PROPERTIES_INITIALIZER;

    /* Initialize bar data */
    bar->currentHeight = (CYGFX_FLOAT)MIN_BAR_HEIGHT;
    bar->targetHeight = (CYGFX_FLOAT)randomRange(MIN_BAR_HEIGHT, MAX_BAR_HEIGHT);

    bar->barIndex = index;
    bar->baseX = BAR_START_X + (index * BAR_SPACING);
    bar->baseY = BAR_START_Y + MAX_BAR_HEIGHT; /* Bottom of the bar area */

    /* Load the surface */
    ret = loadColorSurface(bar->barIndex, &bar->surface);
    if (ret != CYGFX_OK) return ret;

    /* Create window for the bar */
    winProps.topLeftX = bar->baseX;
    winProps.topLeftY = bar->baseY - (CYGFX_S32)bar->currentHeight; /* Position from bottom */
    winProps.width = BAR_WIDTH;
    winProps.height = (CYGFX_U32)bar->currentHeight;
    winProps.layerId = CYGFX_DISP_LAYER_1;
    winProps.features = CYGFX_DISP_FEATURE_MULTI_LAYER;

    ret = CyGfx_DispWinCreate(disp, &winProps, &bar->window);
    if (ret != CYGFX_OK) return ret;

    /* Assign surface to window */
    ret = CyGfx_WinSetSurface(bar->window, CYGFX_DISP_BUFF_TARGET_COLOR_BUFF, &bar->surface);
    if (ret != CYGFX_OK) return ret;

    /* Set blend mode */
    ret = CyGfx_WinSetBlendMode(bar->window, CYGFX_WIN_BLEND_SOURCE_ALPHA);
    if (ret != CYGFX_OK) return ret;

    /* Commit initial window settings */
    ret = CyGfx_WinCommit(bar->window);
    if (ret != CYGFX_OK) return ret;

    return CYGFX_OK;
}

/**********************************************************************************************************************
 * Function Name: updateBarHeight
 * Summary:
 *  updates height of the attached window to the bar
 * Parameters:
 * bar      bar element that needs to be updated
 * Return:
 *  CYGFX_ERROR     returns status
 **********************************************************************************************************************
 */
static CYGFX_ERROR updateBarHeight(DynamicBar* bar)
{
    CYGFX_ERROR ret = CYGFX_OK;
    CYGFX_U32 newHeight;
    CYGFX_S32 newY;

    /* Ensure minimum height */
    if (bar->currentHeight < MIN_BAR_HEIGHT) {
        bar->currentHeight = MIN_BAR_HEIGHT;
    }
    if (bar->currentHeight > MAX_BAR_HEIGHT) {
        bar->currentHeight = MAX_BAR_HEIGHT;
    }

    newHeight = (CYGFX_U32)bar->currentHeight;
    newY = bar->baseY - (CYGFX_S32)newHeight; /* Position from bottom up */

    /* Update window height */
    ret = CyGfx_WinSetAttribute(bar->window, CYGFX_WIN_ATTR_HEIGHT, newHeight);
    if (ret != CYGFX_OK) return ret;

    /* Update window Y position to grow from bottom */
    ret = CyGfx_WinSetAttribute(bar->window, CYGFX_WIN_ATTR_TOPLEFT_Y, newY);
    if (ret != CYGFX_OK) return ret;

    /* Commit changes */
    ret = CyGfx_WinCommit(bar->window);
    if (ret != CYGFX_OK) return ret;

    return CYGFX_OK;
}

/**********************************************************************************************************************
 * Function Name: updateBarLogic
 * Summary:
 *  height update logic witht the random number is done here.
 * Parameters:
 * bar      bar element that needs to be updated
 * Return:
 *  CYGFX_ERROR     returns status
 **********************************************************************************************************************
 */
static CYGFX_ERROR updateBarLogic(DynamicBar* bar)
{
    CYGFX_ERROR ret  = CYGFX_OK;
	CYGFX_BOOL heightChanged = CYGFX_FALSE;

    /* Smooth movement towards target height */
    CYGFX_FLOAT heightDiff = bar->targetHeight - bar->currentHeight;
    if (fabs(heightDiff) > 1.0f) {
        bar->currentHeight += heightDiff * ANIMATION_SPEED * 0.1f; /* Smooth interpolation */
        heightChanged = CYGFX_TRUE;
    } else if (fabs(heightDiff) > 0.1f) {
        bar->currentHeight = bar->targetHeight; /* Snap to target when close */
        heightChanged = CYGFX_TRUE;
    }

    /* Randomly change target height */
    if (randomRange(1, 100) <= RANDOM_CHANCE) {
        bar->targetHeight = (CYGFX_FLOAT)randomRange(MIN_BAR_HEIGHT, MAX_BAR_HEIGHT);
    }

    /* Update window height if changed */
    if (heightChanged) {
        ret = updateBarHeight(bar);
    }

    return ret;
}


/**********************************************************************************************************************
 * Function Name: gfxLoop
 * Summary:
 *  main cyclic function, needs to be called continuously, updated every frame
 * Parameters:
 * bar      bar element that needs to be updated
 * Return:
 *  CYGFX_ERROR     returns status
 **********************************************************************************************************************
 */
CYGFX_ERROR gfxLoop(void)
{
    CYGFX_U32 i;
    CYGFX_ERROR ret = CYGFX_OK;

    /* Update all bars */
    for (i = 0; i < NUM_BARS; i++) {
        ret = updateBarLogic(&bars[i]);
    }

    frameCount++;

    return ret;
}


