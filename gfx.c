/**********************************************************************************************************************
 * Function Name: initWindowsAndSurface
 * Summary:
 *  Initializes all windows with respective properties.
 * Parameters:
 *  none
 * Return:
 *  none
 **********************************************************************************************************************
 */
static CYGFX_ERROR initWindowsAndSurface(void)
{

	CYGFX_ERROR ret = CYGFX_OK;

    CYGFX_DISP_WINDOW_PROPERTIES_S winProps = CYGFX_DISP_WINDOW_PROPERTIES_INITIALIZER;
    
    /* Create background surface */
	ret = CyGfx_SmResetSurfaceObject(&backgroundSurface);
	if (ret != CYGFX_OK) return ret;

	ret = loadBackground();
	if (ret != CYGFX_OK) return ret;

    /* Create background window (Layer 0) */
    winProps.topLeftX = 0;
    winProps.topLeftY = 0;
    winProps.width = DISPLAY_WIDTH;
    winProps.height = DISPLAY_HEIGHT;
    winProps.layerId = CYGFX_DISP_LAYER_0;
    winProps.features = 0;

    ret = CyGfx_DispWinCreate(disp, &winProps, &backgroundWindow);
    if (ret != CYGFX_OK) return ret;

    ret = CyGfx_WinSetSurface(backgroundWindow, CYGFX_DISP_BUFF_TARGET_COLOR_BUFF, &backgroundSurface);
    if (ret != CYGFX_OK) return ret;

    ret = CyGfx_WinCommit(backgroundWindow);
    if (ret != CYGFX_OK) return ret;

    /* Initialize all dynamic bars */
    for (CYGFX_U32 i = 0; i < NUM_BARS; i++) {
        ret = initDynamicBar(&bars[i], i);
        if (ret != CYGFX_OK) {
            printf("ERROR: Failed to initialize dynamic bar %u (0x%08x)\n", (unsigned int)i, (unsigned int)ret);
            return ret;
        }
    }

    return ret;
}




/**********************************************************************************************************************
 * Function Name: initRandom
 * Summary:
 *  Initializes initialise random number
 * Parameters:
 *  none
 * Return:
 *  none
 **********************************************************************************************************************
 */
static void initRandom(void)
{
    srand((unsigned int)time(NULL));
}

/**********************************************************************************************************************
 * Function Name: randomRange
 * Summary:
 *  returns a random number between min and max
 * Parameters:
 * min    minimum value in range
 * max    maximum value in range
 * Return:
 *  CYGFX_S32     random value in range
 **********************************************************************************************************************
 */
static CYGFX_S32 randomRange(CYGFX_S32 min, CYGFX_S32 max)
{
    return min + (rand() % (max - min + 1));
}

/**********************************************************************************************************************
 * Function Name: loadColorSurface
 * Summary:
 *  loaded image into the surface
 * Parameters:
 * index    surface index
 * surface   surface pointer
 * Return:
 *  CYGFX_ERROR     returns status
 **********************************************************************************************************************
 */
static CYGFX_ERROR loadColorSurface(CYGFX_U08 index, CYGFX_SURFACE_OBJECT_S* surface)
{
    CYGFX_ERROR ret;

    ret = CyGfx_SmResetSurfaceObject(surface);
    if (ret != CYGFX_OK) return ret;

    ret = utSurfLoadBitmap(surface, patterns[index], CYGFX_FALSE);
    if (ret != CYGFX_OK) return ret;

    return CYGFX_OK;
}

